@router.patch("/message/{message_id}")
async def edit_message(
    message_id: str, 
    payload: dict, 
    db: Session = Depends(get_db),
    admin: dict = Depends(require_company_admin) # Get admin info
):
    # 1. Fetch message AND check company ownership in one go
    msg = db.query(VisitorMessage).join(VisitorSession).filter(
        VisitorMessage.id == message_id,
        VisitorSession.company_id == admin.get("company_id") # Security Lock
    ).first()

    if not msg:
        # We return 404 even if it exists but belongs to another company 
        # to prevent "ID probing"
        raise HTTPException(status_code=404, detail="Message not found or unauthorized")
    
    new_text = payload.get("text")
    if not new_text:
        raise HTTPException(status_code=400, detail="Content cannot be empty")

    msg.content = new_text
    msg.is_edited = True 
    
    # Update the parent session's last_active so it stays at the top
    msg.session.last_active = datetime.utcnow()
    
    db.commit()
    return {"status": "updated", "message_id": message_id}
@router.post("/send-admin")
async def send_admin_message(
    request: dict, 
    db: Session = Depends(get_db),
    admin: dict = Depends(require_company_admin)
):
    session_id = request.get("session_id")
    admin_company_id = admin.get("company_id")

    # 1. Verify the session belongs to this admin's company
    session = db.query(VisitorSession).filter(
        VisitorSession.id == session_id,
        VisitorSession.company_id == admin_company_id
    ).first()

    if not session:
        raise HTTPException(status_code=403, detail="Unauthorized: Session belongs to another company")

    # 2. Create the manual message
    admin_msg = VisitorMessage(
        session_id=session_id,
        role="assistant", 
        content=request.get("text")
    )
    
    # 3. Update session timestamp for the sidebar "sort by recent" logic
    session.last_active = datetime.utcnow()
    
    db.add(admin_msg)
    db.commit()
    
    return {"status": "sent", "timestamp": session.last_active}