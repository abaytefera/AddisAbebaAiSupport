from App.database.connection import Base

class VisitorSession(Base):
    __tablename__ = "visitor_sessions"

    # The unique ID generated by the Mobile App
    id = Column(UUID(as_uuid=True), primary_key=True, default=uuid.uuid4)
    
    
    company_id = Column(UUID(as_uuid=True), ForeignKey("companies.id"), nullable=False)
    
    created_at = Column(DateTime, default=datetime.utcnow)
    last_active = Column(DateTime, default=datetime.utcnow, onupdate=datetime.utcnow)

    # Relationship to messages
    messages = relationship("VisitorMessage", back_populates="session", cascade="all, delete-orphan")

class VisitorMessage(Base):
    __tablename__ = "visitor_messages"

    id = Column(UUID(as_uuid=True), primary_key=True, default=uuid.uuid4)
    session_id = Column(UUID(as_uuid=True), ForeignKey("visitor_sessions.id"), nullable=False)
    
    role = Column(String, nullable=False)  # "visitor" or "assistant"
    content = Column(Text, nullable=True)
    
    # If the visitor sent a voice note, store the Cloudinary URL here
    file_ = Column(JSON, nullable=True) # Stores {"public_id": "...", "media_url": "..."}
    
    created_at = Column(DateTime, default=datetime.utcnow)

    session = relationship("VisitorSession", back_populates="messages")